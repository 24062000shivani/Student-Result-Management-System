    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        String subject1=(String)jComboBox1.getSelectedItem();
        String subject2=(String)jComboBox2.getSelectedItem();
        String subject3=(String)jComboBox3.getSelectedItem();
        String subject4=(String)jComboBox4.getSelectedItem();
        String subject5=(String)jComboBox5.getSelectedItem();
        String rollno= jTextField1.getText();
        String Marks1=jTextField2.getText();
        String Marks2=jTextField3.getText();
        String Marks3=jTextField4.getText();
        String Marks4 =jTextField5.getText();
        String Marks5 =jTextField6.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Sachin@123");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from studentData where rollno='"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into subject(rollno,subject1,subject2,subject3,subject4,subject5) values('"+rollno+"','"+subject1+"','"+subject2+"','"+subject3+"','"+subject4+"','"+subject5+"')");
                st.executeUpdate("insert into Marks(rollno,Marks1,Marks2,Marks3,Marks4,Marks5)values('"+rollno+"','"+Marks1+"','"+Marks2+"','"+Marks3+"','"+Marks4+"','"+Marks5+"')");
                JOptionPane.showMessageDialog(null,"Result Updated succesfully");
                setVisible(false);
                new InsertResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Student is not registered");
            }
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton7ActionPerformed

